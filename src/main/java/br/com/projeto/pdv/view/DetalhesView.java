    public void filtraData() {

        data_inicio = LocalDateTime.parse(txtDataVenda.getText(), dtf);

        VendaDAO dao = new VendaDAO();
        List<VendaModel> lista = dao.listarVendasPorData(data_inicio, data_fim);

        DefaultTableModel dados = (DefaultTableModel) TabelaDetalhes.getModel();
        dados.setNumRows(0);
        lista.forEach(v -> {
            dados.addRow(new Object[]{
                v.getId(),
                v.getDataVenda(),
                v.getCliente().getNome(),
                v.getTotalVenda()
            });
        });
    }

    public void listar() {

        VendaDAO dao = new VendaDAO();
        List<VendaModel> lista = dao.listarVendas();

        DefaultTableModel dados = (DefaultTableModel) TabelaDetalhes.getModel();
        dados.setNumRows(0);
        lista.forEach(v -> {
            dados.addRow(new Object[]{
                v.getId(),
                v.getDataVenda(),
                v.getCliente().getNome(),
                v.getTotalVenda()
            });
        });
    }

