    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro. Selecione um Funcionario para excluir!");
        } else {
            int op = JOptionPane.showConfirmDialog(null, "Deseja Realmente excluir o Funcionario selecionado?",
                    "Excluir Funcionario", JOptionPane.YES_NO_OPTION );
            if (op != 2 && op != -1) {
                if (op == 0) {
                    FuncionarioModel obj = new FuncionarioModel();
                    obj.setId(Integer.parseInt(txtCodigo.getText()));
                    FuncionarioDAO cDao = new FuncionarioDAO();
                    cDao.excluirFuncionario(obj);

                    limpar();
                    listar();
                }
            }
    }//GEN-LAST:event_btnExcluirActionPerformed
    }
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro. Selecione um Funcionario para Alterar!");
        } else {
            FuncionarioModel obj = new FuncionarioModel();
            obj.setNome(txtNome.getText());
            obj.setRg(txtRg.getText());
            obj.setCpf(txtCpf.getText());
            obj.setEmail(txtEmail.getText());
            obj.setSenha(txtSenha.getText());
            obj.setCargo(txtCargo.getText());
            obj.setNivel_acesso(cbxNvlAcesso.getSelectedItem().toString());
            obj.setTelefone(txtTelefone.getText());
            obj.setCelular(txtCelular.getText());
            obj.setCep(txtCep.getText());
            obj.setEndereco(txtEndereco.getText());
            obj.setNumero(txtNumero.getText());
            obj.setBairro(txtBairro.getText());
            obj.setComplemento(txtComplemento.getText());
            obj.setCidade(txtCidade.getText());
            obj.setEstado(cbxUf.getSelectedItem().toString());

            obj.setId(Integer.parseInt(txtCodigo.getText()));

            FuncionarioDAO cDao = new FuncionarioDAO();
            cDao.alterarFuncionario(obj);

            limpar();
            listar();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

