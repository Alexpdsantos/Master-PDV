    private void TabelaHistoricoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaHistoricoMouseClicked
        // TODO add your handling code here:
        DetalhesView detalhes = new DetalhesView();
        detalhes.txtCliente.setText(TabelaHistorico.getValueAt(TabelaHistorico.getSelectedRow(), 2).toString());
        detalhes.txtTotal.setText(TabelaHistorico.getValueAt(TabelaHistorico.getSelectedRow(), 3).toString());
        detalhes.txtDataVenda.setText(TabelaHistorico.getValueAt(TabelaHistorico.getSelectedRow(), 1).toString());

        int venda_id = Integer.parseInt(TabelaHistorico.getValueAt(TabelaHistorico.getSelectedRow(), 0).toString());

        ItemVendaDAO itemDao = new ItemVendaDAO();
        List<ItemVendaModel> listaItens = itemDao.listarItensPorVenda(venda_id);

        DefaultTableModel dados = (DefaultTableModel) detalhes.TabelaDetalhes.getModel();
        dados.setNumRows(0);

        listaItens.forEach(c -> {
            dados.addRow(new Object[]{
                c.getProduto().getDescricao(),
                c.getQtd(),
                c.getProduto().getPreco(),
                c.getSubTotal()
            });
        });
        detalhes.setVisible(true);
    }//GEN-LAST:event_TabelaHistoricoMouseClicked

