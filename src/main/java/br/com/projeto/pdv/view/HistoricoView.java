    public void filtraData() {

        data_inicio = LocalDateTime.parse(txtDataInicio.getText(), dtf);
        data_fim = LocalDateTime.parse(txtDataFim.getText(), dtf);

        VendaDAO dao = new VendaDAO();
        List<VendaModel> lista = dao.listarVendasPorData(data_inicio, data_fim);

        DefaultTableModel dados = (DefaultTableModel) TabelaHistorico.getModel();
        dados.setNumRows(0);
        lista.forEach(v -> {
            dados.addRow(new Object[]{
                v.getId(),
                v.getDataVenda(),
                v.getCliente().getNome(),
                v.getTotalVenda()
            });
        });
        double soma = 0;
        if (TabelaHistorico.getRowCount() == 0) {
            txtTotalVendido.setText("");
        } else {
            for (int u = 0; u < TabelaHistorico.getRowCount(); u++) {
                soma = soma + Double.valueOf(TabelaHistorico.getValueAt(u, 3).toString());
                txtTotalVendido.setText(String.valueOf(soma));
            }
        }

    }

    public void listar() {

        VendaDAO dao = new VendaDAO();
        List<VendaModel> lista = dao.listarVendas();

        DefaultTableModel dados = (DefaultTableModel) TabelaHistorico.getModel();
        dados.setNumRows(0);
        lista.forEach(v -> {
            dados.addRow(new Object[]{
                v.getId(),
                v.getDataVenda(),
                v.getCliente().getNome(),
                v.getTotalVenda()
            });
        });
    }

